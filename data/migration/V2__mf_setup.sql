CREATE TABLE IF NOT EXISTS mutual_fund_data (
  id SERIAL PRIMARY KEY,
  fund_house CHARACTER(128) NOT NULL UNIQUE,
  scheme_type CHARACTER(128) NOT NULL UNIQUE,
  scheme_category CHARACTER(128) NOT NULL UNIQUE,
  scheme_code CHARACTER(128) NOT NULL UNIQUE,
  scheme_name CHARACTER(128) NOT NULL UNIQUE,
  date DATE NOT NULL,
  nav CHARACTER(50) NOT NULL,
  created_on TIMESTAMP WITHOUT TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_on TIMESTAMP WITHOUT TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
  deleted_on TIMESTAMP WITHOUT TIME ZONE
);
